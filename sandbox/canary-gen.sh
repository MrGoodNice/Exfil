def random_chars(alphabet: str, length: int) -> str:
    # Direct CSPRNG source; no shell $RANDOM/date-derived entropy.
    with open("/dev/urandom", "rb", buffering=0) as rng:
        out = []
        limit = (256 // len(alphabet)) * len(alphabet)
