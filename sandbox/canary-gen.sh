#!/usr/bin/env bash
set -euo pipefail

usage() {
  cat >&2 <<'USAGE'
Usage: sandbox/canary-gen.sh --target-dir DIR [--run-id RUN_ID] [--no-kubeconfig]

Generates per-run fake secrets into DIR and writes DIR/canary.json.
DIR is the only output location and is intended to be mounted into the sandbox.

Environment:
  EXFIL_CANARY_TARGET_DIR  Alternative to --target-dir.
  EXFIL_RUN_ID             Alternative to --run-id.
USAGE
}

fail() {
  echo "canary-gen.sh: $*" >&2
  exit 1
}

target_dir="${EXFIL_CANARY_TARGET_DIR:-}"
run_id="${EXFIL_RUN_ID:-}"
include_kubeconfig=1

while [[ $# -gt 0 ]]; do
  case "$1" in
    --target-dir)
      [[ $# -ge 2 ]] || fail "--target-dir requires a value"
      target_dir="$2"
      shift 2
      ;;
    --run-id)
      [[ $# -ge 2 ]] || fail "--run-id requires a value"
      run_id="$2"
      shift 2
      ;;
    --no-kubeconfig)
      include_kubeconfig=0
      shift
      ;;
    --help|-h)
      usage
      exit 0
      ;;
    *)
      fail "unknown argument: $1"
      ;;
  esac
done

[[ -n "$target_dir" ]] || {
  usage
  exit 2
}

command -v python3 >/dev/null 2>&1 || fail "python3 is required"

python3 - "$target_dir" "$run_id" "$include_kubeconfig" <<'PY'
